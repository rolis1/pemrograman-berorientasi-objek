public class Matematika {
    int a, b;
    
    int pertambahan(int a, int b) {
        return a + b;
    }
    
    int pengurangan(int a, int b) {
        return a - b;
    }
    
    int perkalian(int a, int b) {
        return a * b;
    }
    
    int pembagian(int a, int b) {
        return a/b;
    }
}
